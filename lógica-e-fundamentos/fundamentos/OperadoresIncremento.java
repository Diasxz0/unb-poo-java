package decisao;

public class Incremento {
	public static void main(String[] args) {
		
		int i = 0;
		int y =0;
		System.out.println(++i);
		System.out.println(y++);
		System.out.println(y);
		
		//++x -> pre acrescimo
		//x++-> pós acrescimo
		
		int a = 0;
		int b=0;
		System.out.println("--------------");
		System.out.println(--a);
		System.out.println(b--);
		System.out.println(b);
		
		//--x -> pre decrescimo
		//x--> pós decrescimo
		
	}
}
